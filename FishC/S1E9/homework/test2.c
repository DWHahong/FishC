#include <math.h>
#include <stdio.h>

int main() {
  int a = 10000; // 本金10000元
  float P;

  P = (float)a * (1 + (2.75 / 100) * 5);
  printf("一次性存5年的本息和是%.2f\n", P);

  P = 10000 * (1 + (2.75 / 100) * 3) * (1 + (2.25 / 100) * 2);
  printf("先存 3 年定期，到期后本息再存 2 年定期的本息和是%.2f\n", P);

  P = 10000 * pow((1 + (1.75 / 100)), 5);
  printf("存 1 年定期，到期后本息再存 1 年定期，连续存 5 次的本息和是%.2f\n", P);

  return 0;
}